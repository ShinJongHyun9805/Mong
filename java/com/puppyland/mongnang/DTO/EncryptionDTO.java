package com.puppyland.mongnang.DTO;

public class EncryptionDTO {

    private String f3accessKey;
    private String f3secretKey;
    private String GooglePlayLicenseKey;
    private String key;


    public String getF3accessKey() {
        return f3accessKey;
    }

    public void setF3accessKey(String f3accessKey) {
        this.f3accessKey = f3accessKey;
    }

    public String getF3secretKey() {
        return f3secretKey;
    }

    public void setF3secretKey(String f3secretKey) {
        this.f3secretKey = f3secretKey;
    }

    public String getGooglePlayLicenseKey() {
        return GooglePlayLicenseKey;
    }

    public void setGooglePlayLicenseKey(String googlePlayLicenseKey) {
        GooglePlayLicenseKey = googlePlayLicenseKey;
    }
    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

}
